    private void txtInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyReleased
        String word = txtInput.getText();
        if(!word.isEmpty()){
            boolean result_Full_Numbers = testWord_Full_Numbers(word);
            boolean result_Full_Letters = testWord_Full_Letters(word);
            if(result_Full_Numbers){
                typeWord = "Capicua";
                lbResultTest.setText("Son puros números");
                btnContinue.setEnabled(true);
            }else if(result_Full_Letters){
                typeWord = "Palíndromo";
                lbResultTest.setText("Son puras letras");
                btnContinue.setEnabled(true);
            }else{
                lbResultTest.setText("Cadena Inválida");
                btnContinue.setEnabled(false);
            }
        }else{
            lbResultTest.setText("");
            btnContinue.setEnabled(false);
        }
    }//GEN-LAST:event_txtInputKeyReleased

