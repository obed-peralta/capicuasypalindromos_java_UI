    public boolean testWord_Full_Numbers(String word){
        Pattern pat = Pattern.compile("^\\d+$");
        Matcher mat = pat.matcher(word);
        return mat.matches();
    }
    public boolean testWord_Full_Letters(String word){
        Pattern pat = Pattern.compile("^\\S[a-zA-z]*$");
        Matcher mat = pat.matcher(word);
        return mat.matches();
    }
    
